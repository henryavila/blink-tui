#!/usr/bin/env node
// build-glyph-index.mjs — generate blink's Tier 3 raw glyph index from the
// upstream Nerd Fonts `glyphnames.json`.
//
// WHY THIS EXISTS
//   ui_kits/tui_app/glyph-index.js ships a small, hand-verified SEED (~150
//   glyphs) so the picker and nf() work out of the box. The FULL index (~10k)
//   is too big to hand-maintain and must stay correct across Nerd Fonts
//   releases — so it is GENERATED, never written by hand. Run this after a
//   Nerd Fonts version bump; commit the output and diff it like any artifact.
//
// INPUT
//   glyphnames.json is published in the Nerd Fonts repo (and the `nerdfonts`
//   npm package). Its shape:
//     {
//       "METADATA": { "version": "3.2.1", ... },
//       "fa-rocket": { "char": "\uf135", "code": "f135", "class": "fa-rocket" },
//       ...
//     }
//
// USAGE
//   node scripts/build-glyph-index.mjs <glyphnames.json> [options]
//
//   options
//     --out <file>            write here instead of stdout
//     --format json|js        json (default) → { "fa-rocket": "f135", ... }
//                             js            → an ES module exporting NERD_INDEX
//     --prefixes a,b,c        keep only these sources (e.g. fa,dev,seti,pl,cod,
//                             oct,md,weather) — omit to keep everything
//     --pretty                pretty-print JSON (default: compact)
//
//   examples
//     node scripts/build-glyph-index.mjs glyphnames.json --out glyph-index.full.json
//     node scripts/build-glyph-index.mjs glyphnames.json --format js \
//          --prefixes fa,dev,seti,pl --out glyph-index.full.js
//
//   suggested package.json script:
//     "build:glyphs": "node scripts/build-glyph-index.mjs node_modules/nerdfonts/glyphnames.json --out ui_kits/tui_app/glyph-index.full.json"

import { readFileSync, writeFileSync } from "node:fs";

// ── tiny arg parser ──────────────────────────────────────────────────────────
const argv = process.argv.slice(2);
if (argv.length === 0 || argv[0].startsWith("--")) {
  console.error("usage: node scripts/build-glyph-index.mjs <glyphnames.json> [--out f] [--format json|js] [--prefixes a,b] [--pretty]");
  process.exit(1);
}
const inputPath = argv[0];
const opt = (name, fallback) => {
  const i = argv.indexOf("--" + name);
  return i !== -1 && argv[i + 1] && !argv[i + 1].startsWith("--") ? argv[i + 1] : fallback;
};
const has = (name) => argv.includes("--" + name);

const outPath  = opt("out", null);
const format   = opt("format", "json");
const pretty   = has("pretty");
const prefixes = opt("prefixes", null);
const allow    = prefixes ? new Set(prefixes.split(",").map((s) => s.trim())) : null;

// ── read + transform ─────────────────────────────────────────────────────────
let raw;
try {
  raw = JSON.parse(readFileSync(inputPath, "utf8"));
} catch (e) {
  console.error("✗ cannot read/parse " + inputPath + " — " + e.message);
  process.exit(1);
}

const index = {};
const bySource = {};
let dropped = 0;

for (const name in raw) {
  if (name === "METADATA") continue;
  const entry = raw[name];
  const code = (entry && entry.code ? String(entry.code) : "").toLowerCase();
  if (!/^[0-9a-f]{2,6}$/.test(code)) { dropped++; continue; }

  const source = name.includes("-") ? name.slice(0, name.indexOf("-")) : "misc";
  if (allow && !allow.has(source)) continue;

  index[name] = code;
  bySource[source] = (bySource[source] || 0) + 1;
}

const names = Object.keys(index).sort();
const sorted = {};
for (const n of names) sorted[n] = index[n];

// ── emit ─────────────────────────────────────────────────────────────────────
let output;
if (format === "js") {
  const body = pretty ? JSON.stringify(sorted, null, 2) : JSON.stringify(sorted);
  output =
    "// GENERATED by scripts/build-glyph-index.mjs — do not edit by hand.\n" +
    "// Nerd Fonts version: " + ((raw.METADATA && raw.METADATA.version) || "unknown") + "\n" +
    "export const NERD_INDEX = " + body + ";\n";
} else {
  output = pretty ? JSON.stringify(sorted, null, 2) : JSON.stringify(sorted);
}

if (outPath) {
  writeFileSync(outPath, output);
  console.error("✓ wrote " + names.length + " glyphs → " + outPath);
} else {
  process.stdout.write(output + "\n");
  console.error("✓ " + names.length + " glyphs");
}

// per-source breakdown to stderr (won't pollute piped stdout)
const breakdown = Object.entries(bySource).sort((a, b) => b[1] - a[1])
  .map(([s, n]) => s + ":" + n).join("  ");
console.error("  sources · " + breakdown);
if (dropped) console.error("  skipped · " + dropped + " entries without a valid code");
